#Artimetic Operators +, -, *, /, //, %, **
a = 12
b = 10
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a%b)
print(2**2)

#Comparision Operators >, <,  ==, !=, >=, <=
a = 12
b = 10
print(a == b)
print(a != b)
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)

#Logical Operators and, or, not
bool1 = True
bool2 = False
print(bool1 and bool2)
print(bool1 or bool2)
print(not bool1)

#Assignment Operators =,+=, -=, *=, /=, %=, //=
a = 10
a = a+5
print(a)
a*=5
print(a)

#Identity Operators is, is not
bool1 = True
bool2 = False
print(bool1 is not bool2)
print(bool2 is not bool1)


#Membership operators in, not in
